def build_page(guide, v2_style, compat_css):
    slug = guide["slug"]
    canonical = f"{BASE_URL}/lp/job-seeker/guide/{slug}.html"
    title = guide["title"]
    description = guide["description"]
    h1 = guide["h1"]
    subtitle = guide["subtitle"]
    bc_name = guide["bc_name"]

    bc_payload = {
        "@context": "https://schema.org",
        "@type": "BreadcrumbList",
        "itemListElement": [
            {"@type": "ListItem", "position": 1, "name": "トップ", "item": f"{BASE_URL}/"},
            {"@type": "ListItem", "position": 2, "name": "転職ガイド", "item": f"{BASE_URL}/lp/job-seeker/guide/"},
            {"@type": "ListItem", "position": 3, "name": bc_name, "item": canonical},
        ],
    }
    faq_payload = {
        "@context": "https://schema.org",
        "@type": "FAQPage",
        "mainEntity": [
            {"@type": "Question", "name": q, "acceptedAnswer": {"@type": "Answer", "text": a}}
            for q, a in guide["faq"]
        ],
    }
    article_payload = {
        "@context": "https://schema.org",
        "@type": "Article",
        "headline": h1,
        "description": description,
        "author": {"@type": "Organization", "name": "神奈川ナース転職編集部"},
        "publisher": {"@type": "Organization", "name": "ナースロビー",
                      "logo": {"@type": "ImageObject", "url": f"{BASE_URL}/assets/ogp.png"}},
        "datePublished": "2026-04-20",
        "dateModified": "2026-04-20",
        "mainEntityOfPage": canonical,
    }

    sections_html_parts = []
    for h, body in guide["sections"]:
        sections_html_parts.append(
            "  <section>\n"
            "    <div class=\"container\">\n"
            f"      <h2>{html.escape(h)}</h2>\n"
            f"      <p>{html.escape(body)}</p>\n"
            "    </div>\n"
            "  </section>"
        )
    sections_html = "\n".join(sections_html_parts)

    faq_html_parts = []
    for q, a in guide["faq"]:
        faq_html_parts.append(
            "      <details class=\"faq-item\">\n"
            f"        <summary>{html.escape(q)}</summary>\n"
            f"        <div class=\"faq-body\">{html.escape(a)}</div>\n"
            "      </details>"
        )
    faq_html = "\n".join(faq_html_parts)

    content = HEADER.format(
        title=html.escape(title, quote=True),
        description=html.escape(description, quote=True),
        canonical=html.escape(canonical, quote=True),
        bc_json=json.dumps(bc_payload, ensure_ascii=False),
        faq_json=json.dumps(faq_payload, ensure_ascii=False),
        article_json=json.dumps(article_payload, ensure_ascii=False),
        v2_style=v2_style,
        compat_css=compat_css,
        h1=html.escape(h1),
        subtitle=html.escape(subtitle),
        bc_name=html.escape(bc_name),
        sections_html=sections_html,
        faq_html=faq_html,
    )
    return slug, content


def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("--dry-run", action="store_true")
    ap.add_argument("--apply", action="store_true")
    args = ap.parse_args()
    if not (args.dry_run or args.apply):
        print("--dry-run または --apply", file=sys.stderr)
        sys.exit(1)

    v2_style = load_v2_style()
    generated = 0
    skipped = 0
    for g in GUIDES:
        slug, content = build_page(g, v2_style, GUIDE_COMPAT_CSS)
        path = GUIDE / f"{slug}.html"
        if path.exists():
            print(f"  [skip] {slug}.html (already exists)")
            skipped += 1
            continue
        if args.apply:
            path.write_text(content, encoding="utf-8")
            print(f"  ✓ {slug}.html ({len(content)} bytes)")
        else:
            print(f"  [dry] {slug}.html ({len(content)} bytes)")
        generated += 1

    print(f"\n新規生成 {generated} ページ / 既存スキップ {skipped}")


if __name__ == "__main__":
    main()
